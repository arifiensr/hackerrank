function fairRations(B) {
  // Write your code here
  if (B.length < 3) return 'NO'
  let ration = 0
  for (let i = 0; i < B.length; i++) {
    // if (i > 995 && i < 999) console.log('i = ' + i + ', B[i] = ' + B[i] + ', ration = ' + ration + ', B[i] % 2 === 1 = ' + B[i] % 2) // debug
    if (i === B.length - 1 && B[i] % 2 === 1) return 'NO'
    if (B[i] % 2 === 1) {
      B[i] += 1
      B[i + 1] += 1
      ration += 2
    }
  }
  return ration.toString()
}

